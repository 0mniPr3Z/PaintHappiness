<h2>TARIF AU M²</h2>
<p>Nous pouvons aussi definir le devis en se basant sur la superficie à peindre.</p>
<p>Pour une prestation de peinture comptez un prix moyen TTC au m² est compris entre 25€ et 45€, fournitures et main d'œuvre comprise.<br/>
Le support à peindre (mur ou plafond), l'état du support, l'eventuelle application d'enduit de finition et le nombre de couches de peinture
conditionnent également le prix des travaux au m².</br>
Ce tarif inclut par ailleurs celui des fournitures ainsi que l'application de la peinture.<BR/>
Enfin, l'utilisation de peinture ou de revêtement spécial ou la réalisation d'effets décoratifs uniques engendreront également un surcoût.</p>


